public class Personagem{
    //atributos
    int id, hp, forca, inteligencia;
    String nome;

    //construtor
    public Personagem(String nome, int id, int hp, int forca, int inteligencia) {
        this.nome = nome;
        this.id = id;
        this.hp = hp;
        this.forca = forca;
        this.inteligencia = inteligencia;
    }

    //metodos

}
